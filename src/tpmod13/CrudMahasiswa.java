    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        String nim = input_nim.getText();
        String nama = input_nama.getText();
        String radio = "";
        if(radio_if.isSelected()){
          radio = "IF";
        }else if (radio_se.isSelected()){
          radio = "SE";
        }else if(radio_it.isSelected()){
          radio = "IT";
        }else if(radio_ds.isSelected()){
          radio = "DS";
        }
         try{
           PreparedStatement pr = conn.prepareStatement("INSERT INTO mhs (nim,nama,prodi) VALUES(?,?,?)");
           pr.setString(1, nim);
           pr.setString(2, nama);
           pr.setString(3, radio);
           int rowInserted = pr.executeUpdate(); // buat updatenta
            if(rowInserted > 0){
                JOptionPane.showMessageDialog(this, "data berhasil di tambahkan");
                input_nim.setText("");
                input_nama.setText("");
                radio_if.setSelected(false);
                radio_se.setSelected(false);
                radio_it.setSelected(false);
                radio_ds.setSelected(false);
            }
         }catch(SQLException e){
           Logger.getLogger(e.getMessage()).log(Level.INFO, null, e); // buat info
         }
      
    }//GEN-LAST:event_btn_saveActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
         
         try{
             String[] str = list_data.getSelectedValue().split(" ");
            if(str == null){
                 throw new Exception("silahkan memiliki tab");
            }
         String nim = str[0];
            String sql = "DELETE FROM mhs WHERE nim ="+nim+";";
            PreparedStatement st = conn.prepareStatement(sql);
            int output = st.executeUpdate();
            this.model.clear();
            this.GetData();
            if (output > 0){
                JOptionPane.showMessageDialog(this, "data berhasil di hapus");
            } 
         }catch(SQLException e){
             JOptionPane.showMessageDialog(this, e.getMessage());
         }catch(Exception e){
              JOptionPane.showMessageDialog(this, e.getMessage());
         }
    }//GEN-LAST:event_btn_hapusActionPerformed

