    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:\
      
        String nim = input_nim.getText();
        String nama = input_nama.getText();
        String radio = "";
        if(radio_if.isSelected()){
          radio = "IF";
        }else if (radio_se.isSelected()){
          radio = "SE";
        }else if(radio_it.isSelected()){
          radio = "IT";
        }else if(radio_ds.isSelected()){
          radio = "DS";
        }
         try{
           PreparedStatement pr = conn.prepareStatement("UPDATE mhs set nim = ? , nama = ? , prodi = ? where nim = ?");
           pr.setString(1, nim);
           pr.setString(2, nama);
           pr.setString(3, radio);
           pr.setString(4, nim);
           int rowInserted = pr.executeUpdate(); // buat updatenta
            if(rowInserted > 0){
                JOptionPane.showMessageDialog(this, "data berhasil di update");
                input_nim.setText("");
                input_nama.setText("");
                radio_if.setSelected(false);
                radio_se.setSelected(false);
                radio_it.setSelected(false);
                radio_ds.setSelected(false);
                // button and view  management
                btn_update.setEnabled(false);
                btn_save.setEnabled(true);
                tab_controls.setSelectedIndex(0); // set tabbpane 
                this.GetData(); // get data again
            }
         }catch(SQLException e){
           Logger.getLogger(e.getMessage()).log(Level.INFO, null, e); // buat info
         }
    }//GEN-LAST:event_btn_updateActionPerformed
    public void GetData(){
        try{
            String sql = "SELECT * FROM mhs";
            PreparedStatement st = conn.prepareStatement(sql);
            ResultSet output = st.executeQuery();
            this.model.clear();
            while(output.next()){ // loop result set nya
                this.model.addElement(output.getString("nim") +" "+ output.getString("nama") +" "+ output.getString("prodi") );
            }
            
        }catch(SQLException e){
            Logger.getLogger(e.getMessage()).log(Level.INFO, null, e); // buat info
        }
    }
